    @Override
    public Flux<POI> searchByKeyword(String city, String district, String keyword) {
        return Flux.fromStream(getStream(city, district))
                .filter(p -> matchesKeyword(p, keyword));
    }

    private boolean matchesKeyword(POI p, String keyword) {
        // Direct match
        if (p.name().contains(keyword)) return true;
        if (p.tags().stream().anyMatch(t -> t.contains(keyword))) return true;
        if (p.description().contains(keyword)) return true;
        // Fuzzy match: check keyword's associated tags
        var associated = KEYWORD_TO_TAGS.get(keyword);
        if (associated != null) {
            return p.tags().stream().anyMatch(t ->
                    associated.stream().anyMatch(a -> t.contains(a)));
        }
        // Try substrings of the keyword (handle compound words like "喝一口酒" → "喝酒", "酒")
        for (int len = keyword.length(); len >= 2; len--) {
            for (int i = 0; i + len <= keyword.length(); i++) {
                var sub = keyword.substring(i, i + len);
                var subTags = KEYWORD_TO_TAGS.get(sub);
                if (subTags != null && p.tags().stream().anyMatch(t ->
                        subTags.stream().anyMatch(a -> t.contains(a)))) {
                    return true;
                }
            }
        }
        // Try splitting by common delimiter characters
        for (var part : keyword.split("[，,、\\s]+")) {
            if (part.length() < 2) continue;
            var partTags = KEYWORD_TO_TAGS.get(part);
            if (partTags != null && p.tags().stream().anyMatch(t ->
                    partTags.stream().anyMatch(a -> t.contains(a)))) {
                return true;
            }
        }
        return false;
    }

    @Override
    public Flux<POI> searchWithinBudget(String city, String district, double maxCost) {
        return Flux.fromStream(getStream(city, district))
                .filter(p -> p.avgCost() <= maxCost);
    }

    @Override
    public Mono<POI> findById(String id) {
        return Mono.justOrEmpty(poiById.get(id));
    }

    @Override
    public Flux<POI> getAllByCity(String city) {
        return Flux.fromIterable(poiByCity.getOrDefault(city, List.of()));
    }

    @Override
    public Flux<POI> getByDistrict(String city, String district) {
        return Flux.fromIterable(
                poiByCity.getOrDefault(city, List.of()).stream()
                        .filter(p -> p.district().equals(district))
                        .toList()
        );
    }

    @Override
    public List<String> getAvailableCities() {
        return List.copyOf(poiByCity.keySet());
    }

    @Override
    public List<String> getAvailableDistricts(String city) {
        return poiByCity.getOrDefault(city, List.of()).stream()
                .map(POI::district)
                .distinct()
                .toList();
    }

    // Placeholder district names from frontend Q&A that aren't real locations
    private static final Set<String> PLACEHOLDER_DISTRICTS = Set.of(
            "指定商圈", "当前位置附近", "当前位置", "公司附近", "回家路上", "地铁站附近", "商圈附近"
    );

    private Stream<POI> getStream(String city, String district) {
        Stream<POI> stream = poiByCity.getOrDefault(city, List.of()).stream();
        // Skip filtering for placeholder districts — treat as city-wide search
        if (district != null && !district.isBlank() && !PLACEHOLDER_DISTRICTS.contains(district)) {
            var filtered = stream.filter(p -> {
                if (p.district().equals(district)) return true;
                if (p.name().contains(district)) return true;
                if (p.district().contains(district) || district.contains(p.district())) return true;
                if (p.address() != null && p.address().contains(district)) return true;
                return false;
            }).toList();
            // Fallback: if district filter yields 0 results, search city-wide
            if (!filtered.isEmpty()) {
                return filtered.stream();
            }
            log.info("MockDataService: district '{}' matched 0 POIs, falling back to city-wide search", district);
            return poiByCity.getOrDefault(city, List.of()).stream();
        }
        return stream;
    }

